 let c=4;
 let d=3;
 //Add and Assign
 c +=d; //c=c+d ->c=7
 console.log(c); //7

 
 //Subtract and Assign
 c -=d; //c=c-d ->c=1
 console.log(c); //1

 //Multiply and Assign
 c *=d; //c=c*d ->c=12
 console.log(c); //12

 //Divide and Assign
 c /=d; //c=c/d ->c=4
 console.log(c); //4

 //Module and Assign
 c %=d; //c=c-d ->c=1
 console.log(c); //1

 //Exponential and Assign
 c=2;
 c **=d; //c=c**d ->2=1
 console.log(c); //1